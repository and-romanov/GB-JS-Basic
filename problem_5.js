function sum(a, b) {
    return a + b;
};

function differrence(a, b) {
    return a - b;
};

function multiplication(a, b) {
    return a * b;
};

function division(a, b) {
    return a / b;
};

console.log(sum(3, 5));
console.log(differrence(3, 5));
console.log(multiplication(3, 5));
console.log(division(3, 5));